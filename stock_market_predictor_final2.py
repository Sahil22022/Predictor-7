def make_windows_multifeat(X_scaled: np.ndarray, y_scaled: np.ndarray, lookback: int):
    Xw, yw = [], []
    for i in range(lookback, len(X_scaled)):
        Xw.append(X_scaled[i - lookback:i, :])
        yw.append(y_scaled[i, 0])
    return np.array(Xw), np.array(yw).reshape(-1, 1)


def train_val_split_time(X, y, val_ratio=0.2):
    n = len(X)
    split = int(n * (1 - val_ratio))
    return X[:split], y[:split], X[split:], y[split:]


def build_gru(lookback: int, n_features: int, units=64, dropout=0.2, lr=1e-3):
    model = keras.Sequential([
        layers.Input(shape=(lookback, n_features)),
        layers.GRU(units, return_sequences=True),
        layers.Dropout(dropout),
        layers.GRU(units),
        layers.Dropout(dropout),
        layers.Dense(64, activation="relu"),
        layers.Dense(1),
    ])
    model.compile(optimizer=keras.optimizers.Adam(lr), loss="mse")
    return model


def plot_actual_vs_pred(dates, actual, pred, title):
    plt.figure()
    plt.plot(dates, actual, label="Actual", linewidth=2)
    plt.plot(dates, pred, label="Predicted", linewidth=2)
    plt.title(title)
    plt.xlabel("Date")
    plt.ylabel("Close")
    plt.grid(True, alpha=0.3)
    plt.legend()
    plt.show()


def forecast_future_close_iterative(
    model,
    close_history: pd.DataFrame,   # index datetime, col Close
    feature_cols,
    lookback: int,
    steps: int,
    feature_scaler: MinMaxScaler,
    target_scaler: MinMaxScaler,
):
    tmp = close_history[["Close"]].copy().sort_index()
    preds = []
    f_dates = []
    last_date = tmp.index[-1]

    for _ in range(steps):
        feats = add_indicators_from_close(tmp)
        feats = feats[feature_cols + ["Close"]].dropna()

        x_window = feats.iloc[-lookback:][feature_cols].values
        x_scaled = feature_scaler.transform(x_window).reshape(1, lookback, len(feature_cols))

        pred_scaled = model.predict(x_scaled, verbose=0)[0, 0]
        pred_close = target_scaler.inverse_transform([[pred_scaled]])[0, 0]

        next_date = last_date + pd.tseries.offsets.BDay(1)
        tmp.loc[next_date, "Close"] = float(pred_close)

        preds.append(float(pred_close))
        f_dates.append(next_date)
        last_date = next_date

    return np.array(preds), pd.DatetimeIndex(f_dates)

