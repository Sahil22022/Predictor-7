epochs = st.slider("Epochs", 3, 50, 10, 1)
horizon = st.slider("Forecast days", 1, 30, 7, 1)

@st.cache_data(ttl=900)
def fetch_data(ticker, start, end):
    df = yf.download(ticker, start=str(start), end=str(end), progress=False, auto_adjust=False)
    if isinstance(df.columns, pd.MultiIndex):
        df.columns = df.columns.get_level_values(0)
    if "Close" not in df.columns and "Adj Close" in df.columns:
        df["Close"] = df["Adj Close"]
    return df.dropna()

def make_windows(arr, lb):
    X, y = [], []
    for i in range(lb, len(arr)):
        X.append(arr[i-lb:i, 0])
        y.append(arr[i, 0])
    return np.array(X).reshape(-1, lb, 1), np.array(y).reshape(-1, 1)

if st.button("Run"):
    df = fetch_data(ticker, start, end)
    if df.empty or "Close" not in df.columns:
        st.error("No data / Close not found")
        st.stop()

    close = df[["Close"]].astype(float)
    scaler = MinMaxScaler()
    s = scaler.fit_transform(close.values)

    X, y = make_windows(s, lookback)
    split = int(len(X) * 0.8)
    X_train, y_train = X[:split], y[:split]
    X_val, y_val = X[split:], y[split:]

    model = Sequential([
        layers.Input(shape=(lookback, 1)),
        layers.GRU(32, return_sequences=True),
        layers.GRU(32),
        layers.Dense(1)
    ])
    model.compile(optimizer="adam", loss="mse")
    model.fit(X_train, y_train, validation_data=(X_val, y_val), epochs=epochs, batch_size=64, verbose=0)

    pred = model.predict(X_val, verbose=0)
    y_true = scaler.inverse_transform(y_val).ravel()
    y_pred = scaler.inverse_transform(pred).ravel()

    mae = mean_absolute_error(y_true, y_pred)
    rmse = np.sqrt(mean_squared_error(y_true, y_pred))
    st.write(f"MAE: {mae:.2f} | RMSE: {rmse:.2f}")

    chart_df = pd.DataFrame({"Actual": y_true, "Predicted": y_pred})
    st.line_chart(chart_df)

    # iterative forecast
    win = s[-lookback:].copy()
    fut = []
    for _ in range(horizon):
        p = model.predict(win.reshape(1, lookback, 1), verbose=0)[0,0]
        fut.append(p)
        win = np.vstack([win[1:], [[p]]])

    fut_price = scaler.inverse_transform(np.array(fut).reshape(-1,1)).ravel()
    st.subheader("Future forecast")
    st.dataframe(pd.DataFrame({"Day": np.arange(1, horizon+1), "PredictedClose": fut_price}))