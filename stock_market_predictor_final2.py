def on_run(_):
    with out:
        clear_output(wait=True)
        try:
            ticker = ticker_w.value.strip().upper()
            start  = start_w.value.strip()
            end    = end_w.value.strip()
            lookback = int(lookback_w.value)
            epochs   = int(epochs_w.value)
            batch    = int(batch_w.value)
            units    = int(units_w.value)
            dropout  = float(dropout_w.value)
            val_ratio = float(val_w.value)
            horizon  = int(horizon_w.value)

            print(f"Downloading latest data for {ticker} ...")
            df = fetch_ohlcv(ticker, start, end)

            # Add indicators
            df_ind = add_indicators_from_close(df)

            # Feature set
            feature_cols = [
                "pct_return", "log_return",
                "sma10", "sma20", "ema10", "ema20", "ema50",
                "vol20",
                "rsi14",
                "macd", "macd_signal", "macd_hist",
                "bb_mid", "bb_upper", "bb_lower", "bb_width",
                "dayofweek"
            ]

            # Keep needed cols and drop initial NaN rows from rolling calculations
            df_ind = df_ind[feature_cols + ["Close"]].dropna()

            if len(df_ind) < (lookback + 80):
                raise ValueError(
                    f"Not enough rows after indicators ({len(df_ind)}). "
                    f"Use earlier start date or reduce lookback."
                )

            # Time split index (also used for scaler fit to reduce leakage)
            split_row = int(len(df_ind) * (1 - val_ratio))
            train_part = df_ind.iloc[:split_row]

            feature_scaler = MinMaxScaler()
            target_scaler = MinMaxScaler()
            feature_scaler.fit(train_part[feature_cols].values)
            target_scaler.fit(train_part[["Close"]].values)

            X_all = feature_scaler.transform(df_ind[feature_cols].values)
            y_all = target_scaler.transform(df_ind[["Close"]].values)

            X, y = make_windows_multifeat(X_all, y_all, lookback)
            X_train, y_train, X_val, y_val = train_val_split_time(X, y, val_ratio=val_ratio)

            model = build_gru(
                lookback=lookback,
                n_features=len(feature_cols),
                units=units,
                dropout=dropout,
                lr=1e-3
            )

            callbacks = [
                keras.callbacks.EarlyStopping(monitor="val_loss", patience=8, restore_best_weights=True),
                keras.callbacks.ReduceLROnPlateau(monitor="val_loss", factor=0.5, patience=4, min_lr=1e-5),
            ]

            print("Training model ...")
            hist = model.fit(
                X_train, y_train,
                validation_data=(X_val, y_val),
                epochs=epochs,
                batch_size=batch,
                verbose=0,
                callbacks=callbacks
            )

            # Loss curve
            plt.figure(figsize=(12, 4))
            plt.plot(hist.history["loss"], label="train")
            plt.plot(hist.history["val_loss"], label="val")
            plt.title("Training Loss (MSE)")
            plt.xlabel("Epoch")
            plt.ylabel("MSE")
            plt.grid(True, alpha=0.3)
            plt.legend()
            plt.show()

            # Validation predictions (back to price space)
            y_val_pred_scaled = model.predict(X_val, verbose=0).ravel()
            y_val_true_scaled = y_val.ravel()

            y_val_pred = target_scaler.inverse_transform(y_val_pred_scaled.reshape(-1, 1)).ravel()
            y_val_true = target_scaler.inverse_transform(y_val_true_scaled.reshape(-1, 1)).ravel()

            mae = mean_absolute_error(y_val_true, y_val_pred)
            rmse = np.sqrt(mean_squared_error(y_val_true, y_val_pred))
            print(f"Validation MAE: {mae:.2f}")
            print(f"Validation RMSE: {rmse:.2f}")

            # Dates for validation targets
            target_dates = df_ind.index[lookback:]     # one date per y
            val_dates = target_dates[len(y_train):]    # validation segment
            plot_actual_vs_pred(val_dates, y_val_true, y_val_pred, f"{ticker} Validation: Actual vs Predicted")

            # Future forecast
            close_history = df_ind[["Close"]].copy()
            future_prices, future_dates = forecast_future_close_iterative(
                model=model,
                close_history=close_history,
                feature_cols=feature_cols,
                lookback=lookback,
                steps=horizon,
                feature_scaler=feature_scaler,
                target_scaler=target_scaler,
            )

            # Plot history + forecast
            tail_n = min(250, len(df_ind))
            plt.figure()
            plt.plot(df_ind.index[-tail_n:], df_ind["Close"].values[-tail_n:], label="Historical Close", linewidth=2)
            plt.plot(future_dates, future_prices, label=f"Forecast (+{horizon} business days)", linewidth=2)
            plt.title(f"{ticker} Forecast")
            plt.xlabel("Date")
            plt.ylabel("Close")
            plt.grid(True, alpha=0.3)
            plt.legend()
            plt.show()

            forecast_df = pd.DataFrame({"Date": future_dates, "PredictedClose": future_prices})
            display(forecast_df)

            print("\nDone. Change ticker/settings and click Run again.")

        except Exception as e:
            print("Error:", str(e))


