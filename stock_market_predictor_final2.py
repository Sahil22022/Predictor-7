def fetch_ohlcv(ticker: str, start: str, end: str, interval: str = "1d") -> pd.DataFrame:
    df = yf.download(
        ticker,
        start=start,
        end=end,
        interval=interval,
        progress=False,
        auto_adjust=False,
        group_by="column",
    )

    if df is None or df.empty:
        raise ValueError("No data returned. Check ticker/date range.")

    # Handle MultiIndex columns robustly
    if isinstance(df.columns, pd.MultiIndex):
        lvl0 = [str(c).strip() for c in df.columns.get_level_values(0)]
        lvl1 = [str(c).strip() for c in df.columns.get_level_values(1)]

        # If OHLC names exist in level 0, keep level 0; else use level 1
        if "Close" in lvl0 or "Adj Close" in lvl0:
            df.columns = lvl0
        else:
            df.columns = lvl1

    # Normalize names
    df.columns = [str(c).strip() for c in df.columns]
    name_map = {
        "open": "Open",
        "high": "High",
        "low": "Low",
        "close": "Close",
        "adj close": "Adj Close",
        "volume": "Volume",
    }
    df = df.rename(columns={c: name_map.get(c.lower(), c) for c in df.columns})

    # Ensure Close exists
    if "Close" not in df.columns:
        if "Adj Close" in df.columns:
            df["Close"] = df["Adj Close"]
        else:
            raise ValueError(f"'Close' not found. Columns: {list(df.columns)}")

    # Force numeric where possible
    for c in df.columns:
        df[c] = pd.to_numeric(df[c], errors="ignore")

    df = df.dropna()
    df.index = pd.to_datetime(df.index)
    return df


def add_indicators_from_close(df: pd.DataFrame) -> pd.DataFrame:
    out = df.copy()

    if "Close" not in out.columns:
        raise ValueError(f"'Close' missing. Available columns: {list(out.columns)}")

    close = out["Close"]
    if isinstance(close, pd.DataFrame):  # safety for duplicate close columns
        close = close.iloc[:, 0]
    close = pd.to_numeric(close, errors="coerce")

    # Returns
    out["pct_return"] = close.pct_change()
    out["log_return"] = np.log(close).diff()

    # Trend
    out["sma10"] = close.rolling(10).mean()
    out["sma20"] = close.rolling(20).mean()
    out["ema10"] = close.ewm(span=10, adjust=False).mean()
    out["ema20"] = close.ewm(span=20, adjust=False).mean()
    out["ema50"] = close.ewm(span=50, adjust=False).mean()

    # Volatility
    out["vol20"] = out["pct_return"].rolling(20).std()

    # RSI(14)
    delta = close.diff()
    gain = delta.clip(lower=0)
    loss = (-delta).clip(lower=0)
    avg_gain = gain.rolling(14).mean()
    avg_loss = loss.rolling(14).mean()
    rs = avg_gain / (avg_loss + 1e-9)
    out["rsi14"] = 100 - (100 / (1 + rs))

    # MACD(12,26,9)
    ema12 = close.ewm(span=12, adjust=False).mean()
    ema26 = close.ewm(span=26, adjust=False).mean()
    out["macd"] = ema12 - ema26
    out["macd_signal"] = out["macd"].ewm(span=9, adjust=False).mean()
    out["macd_hist"] = out["macd"] - out["macd_signal"]

    # Bollinger(20,2)
    bb_mid = close.rolling(20).mean()
    bb_std = close.rolling(20).std()
    out["bb_mid"] = bb_mid
    out["bb_upper"] = bb_mid + 2 * bb_std
    out["bb_lower"] = bb_mid - 2 * bb_std
    out["bb_width"] = (out["bb_upper"] - out["bb_lower"]) / (bb_mid + 1e-9)

    # Calendar
    out["dayofweek"] = out.index.dayofweek

    out = out.replace([np.inf, -np.inf], np.nan)
    return out

